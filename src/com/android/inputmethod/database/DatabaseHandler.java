	// All Static variables
	// Database Version
	private static final int DATABASE_VERSION = 1;

	// Database Name
	private static final String DATABASE_NAME = "CustomEmojiDB.db";

	private static final String TABLE_EMOJI = "Emojis";

	private static final String KEY_KEYWORD = "keyword";
	private static final String KEY_UNICODE = "emojiUnicode";
	SQLiteDatabase database;

	public DatabaseHandler(Context context) {
		super(context, DATABASE_NAME, null, DATABASE_VERSION);
	}

	// Creating Tables
	@Override
	public void onCreate(SQLiteDatabase db) {
		String CREATE_EMOJI_TABLE = "CREATE TABLE " + TABLE_EMOJI + "("
				+ KEY_KEYWORD + " TEXT," + KEY_UNICODE + " TEXT" + ")";
		db.execSQL(CREATE_EMOJI_TABLE);
		database = db;
		addEmojisToDatabase();
	}

	private void add(String keyword, String unicode) {
		CustomEmoji emoji = new CustomEmoji();
		emoji.setEmoji(keyword, unicode);
		addEmoji(emoji);
	}

	// Upgrading database
	@Override
	public void onUpgrade(SQLiteDatabase db, int oldVersion, int newVersion) {
		// Drop older table if existed
		db.execSQL("DROP TABLE IF EXISTS " + TABLE_EMOJI);

		// Create tables again
		onCreate(db);
	}

	public void addEmoji(CustomEmoji emoji) {
		ContentValues values = new ContentValues();
		values.put(KEY_KEYWORD, emoji.getKeyword());
		values.put(KEY_UNICODE, emoji.getEmojiUnicode());
		database.insert(TABLE_EMOJI, null, values);

	}

